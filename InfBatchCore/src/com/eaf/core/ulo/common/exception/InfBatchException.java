package com.eaf.core.ulo.common.exception;

public class InfBatchException extends Exception {
	public InfBatchException() {
		super();
	}
	public InfBatchException(String message) {
		super(message);
	}
	public InfBatchException(String message, Throwable cause) {
		super(message, cause);
	}
	public InfBatchException(Throwable cause) {
		super(cause);
	}
}
