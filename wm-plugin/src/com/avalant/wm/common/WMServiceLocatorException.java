package com.avalant.wm.common;

public class WMServiceLocatorException extends Exception{
	public WMServiceLocatorException() {
		super();		 
	}
	public WMServiceLocatorException(String message) {
		super(message);
	 
	}
	public WMServiceLocatorException(String message, Throwable cause) {
		super(message, cause);
	 
	}
	public WMServiceLocatorException(Throwable cause) {
		super(cause);		
	}
}
